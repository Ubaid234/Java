package Operators;

public class Shortcut {
    public static void main(String[] args) {
        int number = 2;
        int numberTwo = 10;

        numberTwo += number;
        System.out.println(numberTwo);
        numberTwo -= number;
        System.out.println(numberTwo);
        numberTwo *= number;
        System.out.println(numberTwo);
        numberTwo /= number;
        System.out.println(numberTwo);
        numberTwo %= number;
        System.out.println(numberTwo);

        
    }
}
