package Operators;

public class ComparisonOperators {
    public static void main(String[] args) {
        
        // COMPARISION OPERATORS
        System.out.println(10 > 10);
        System.out.println(10 >= 10);
        System.out.println(10 < 10);
        System.out.println(10 <= 10);
        System.out.println(10 == 10);
        System.out.println(10 != 10);
        
    }
}
